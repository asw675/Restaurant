package com.bignerdranch.android.restaurant.Model;

public class User {
    private int user_id;
    private String user_name;
    private String password;
    private float user_price;
    private int user_cursine;
    private int user_taste;
    private int user_heathly;
    private int user_orders;
    private String user_menu;

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public float getUser_price() {
        return user_price;
    }

    public void setUser_price(float user_price) {
        this.user_price = user_price;
    }

    public int getUser_cursine() {
        return user_cursine;
    }

    public void setUser_cursine(int user_cursine) {
        this.user_cursine = user_cursine;
    }

    public int getUser_taste() {
        return user_taste;
    }

    public void setUser_taste(int user_taste) {
        this.user_taste = user_taste;
    }

    public int getUser_heathly() {
        return user_heathly;
    }

    public void setUser_heathly(int user_heathly) {
        this.user_heathly = user_heathly;
    }

    public int getUser_orders() {
        return user_orders;
    }

    public void setUser_orders(int user_orders) {
        this.user_orders = user_orders;
    }

    public String getUser_menu() {
        return user_menu;
    }

    public void setUser_menu(String user_menu) {
        this.user_menu = user_menu;
    }
}
